function silver(element) {
  element.style.setProperty('background-color', 'silver');
}

function red(element) {
  element.style.setProperty('background-color', 'red');
}

function green(element) {
  element.style.setProperty('background-color', 'green');
}

function blue(element) {
  element.style.setProperty('background-color', 'blue');
}

export { silver as default, red, green, blue };
